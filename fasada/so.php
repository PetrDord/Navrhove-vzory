<?php
 

 
 class nasoby{
     
    /**
     * nasobení
     * 
     * vypočte a vrátí součin dvou čísel
     * 
     * @param float
     * @param float
     * @return float
     */
    public static function nasobeni($a, $b): float
    {
        return $a * $b;
    }

    /**
     * dělení
     * 
     * vypočte a vrátí podíl dvou čísel
     * 
     * @param float
     * @param float
     * @return float
     */
    public static function deleni($a, $b): float
    {
        return $a/$b;
    }

}