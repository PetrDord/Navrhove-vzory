<?php

class citani{

    /**
     * přičítání
     * 
     * vypočte a vrátí součet dvou čísel
     * 
     * @return float
     */
    public static function pricitani($a, $b): float
    {
        return $a + $b;
    }
 
    /**
     * odčítaní
     * 
     * vypočte a vrátí rozdíl dvou čísel
     * 
     * @return float
     */
    public static function odcitani($a, $b): float
    {
        return $a - $b;
    }

}
